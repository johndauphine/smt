func TestDeterministicCreateTableMSSQLToPostgres(t *testing.T) {
	renderer := newDeterministicDDL()
	table := &driver.Table{
		Schema:     "dbo",
		Name:       "PostLinks",
		PrimaryKey: []string{"Id"},
		Columns: []driver.Column{
			{Name: "Id", DataType: "int", Precision: 10, IsIdentity: true, IsNullable: false},
			{Name: "CreationDate", DataType: "datetime", IsNullable: false},
			{Name: "RelatedPostId", DataType: "int", Precision: 10, IsNullable: false},
			{Name: "LinkTypeId", DataType: "int", Precision: 10, IsNullable: false},
		},
	}

	ddl, columnTypes, err := renderer.createTable(table, "public", false)
	if err != nil {
		t.Fatalf("createTable: %v", err)
	}

	assertContains(t, ddl, `CREATE TABLE "public"."postlinks"`)
	assertContains(t, ddl, `"id" integer GENERATED BY DEFAULT AS IDENTITY`)
	assertContains(t, ddl, `"creationdate" timestamp without time zone NOT NULL`)
	assertContains(t, ddl, `CONSTRAINT "pk_postlinks" PRIMARY KEY ("id")`)
	if got := columnTypes["Id"]; got != "integer GENERATED BY DEFAULT AS IDENTITY" {
		t.Fatalf("columnTypes[Id] = %q", got)
	}
}

