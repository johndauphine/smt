func TestDeterministicMySQLDefaultsToPostgres(t *testing.T) {
	renderer := newDeterministicDDL()
	for _, tc := range []struct {
		name string
		col  driver.Column
		want string
	}{
		{
			name: "uuid",
			col:  driver.Column{Name: "id", DataType: "char", MaxLength: 36, DefaultExpression: "(uuid())"},
			want: "gen_random_uuid()",
		},
		{
			name: "timestamp precision",
			col:  driver.Column{Name: "created_at", DataType: "datetime", DefaultExpression: "current_timestamp(6)"},
			want: "CURRENT_TIMESTAMP",
		},
		{
			name: "enum bare word",
			col:  driver.Column{Name: "customer_type", DataType: "enum", DefaultExpression: "individual"},
			want: "'individual'",
		},
	} {
		t.Run(tc.name, func(t *testing.T) {
			got, err := renderer.defaultExpression(tc.col)
			if err != nil {
				t.Fatalf("defaultExpression: %v", err)
			}
			if got != tc.want {
				t.Fatalf("defaultExpression = %q, want %q", got, tc.want)
			}
		})
	}
}

