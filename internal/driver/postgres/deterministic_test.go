func TestDeterministicFinalizationDDL(t *testing.T) {
	renderer := newDeterministicDDL()
	table := &driver.Table{Name: "Votes"}

	idxDDL, err := renderer.createIndex(table, &driver.Index{
		Name:     "IX_Votes_PostId",
		Columns:  []string{"PostId"},
		IsUnique: false,
	}, "public")
	if err != nil {
		t.Fatalf("createIndex: %v", err)
	}
	assertContains(t, idxDDL, `CREATE INDEX "ix_votes_postid" ON "public"."votes" ("postid")`)

	fkDDL, err := renderer.createForeignKey(table, &driver.ForeignKey{
		Name:       "FK_Votes_Posts",
		Columns:    []string{"PostId"},
		RefTable:   "Posts",
		RefColumns: []string{"Id"},
		OnDelete:   "NO ACTION",
	}, "public")
	if err != nil {
		t.Fatalf("createForeignKey: %v", err)
	}
	assertContains(t, fkDDL, `ALTER TABLE "public"."votes" ADD CONSTRAINT "fk_votes_posts" FOREIGN KEY ("postid") REFERENCES "public"."posts" ("id")`)

	checkDDL, err := renderer.createCheckConstraint(table, &driver.CheckConstraint{
		Name:       "CK_Votes_BountyAmount",
		Definition: "([BountyAmount]>(0))",
	}, "public")
	if err != nil {
		t.Fatalf("createCheckConstraint: %v", err)
	}
	assertContains(t, checkDDL, `ALTER TABLE "public"."votes" ADD CONSTRAINT "ck_votes_bountyamount" CHECK ("bountyamount">(0))`)
}

