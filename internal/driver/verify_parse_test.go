// TestBuildVerifyParsePrompt_PinsExtractionRules is the regression guard
// for the parse prompt. The model needs unambiguous extraction rules — drop
// any of these phrases and the parser starts emitting wrong shapes (e.g.
// data_type with parens, max_length swapped with precision).
func TestBuildVerifyParsePrompt_PinsExtractionRules(t *testing.T) {
	prompt := buildVerifyParsePrompt("CREATE TABLE x (id integer);", "postgres")

	for _, needle := range []string{
		// Output schema enforcement.
		"OUTPUT SCHEMA",
		"\"columns\":",
		"\"is_identity\":",
		"\"default_expression\":",
		"\"is_computed\":",
		// Extraction rules — each addresses a known parser failure mode.
		"EXTRACTION RULES",
		"data_type carries ONLY the base type name",
		"`VARCHAR(20)` → `varchar`",
		"`NUMERIC(18,4)` → `numeric`",
		"Unbounded text types",
		"max_length=0",
		"is_identity is TRUE for any auto-generated PK default mechanism",
		"GENERATED BY DEFAULT AS IDENTITY",
		"IDENTITY(seed, increment)",
		"AUTO_INCREMENT",
		// Output discipline.
		"OUTPUT CONSTRAINTS",
		"single JSON object starting with `{`",
		"No markdown fences",
		// Wiring.
		"=== TARGET DIALECT ===",
		"postgres",
		"=== TARGET DDL ===",
		"CREATE TABLE x (id integer);",
	} {
		if !strings.Contains(prompt, needle) {
			t.Errorf("parse prompt missing required phrase %q", needle)
		}
	}
}

// mockParseServer wraps a httptest server returning fixed content and binds
// an AITypeMapper to it. Mirrors verifyMockServer in ai_verify_test.go.
func mockParseServer(t *testing.T, content string) *AITypeMapper {
	t.Helper()
	mapper, _ := mockParseServerSequence(t, content)
	return mapper
}

func mockParseServerSequence(t *testing.T, contents ...string) (*AITypeMapper, *atomic.Int32) {
	t.Helper()
	if len(contents) == 0 {
		t.Fatal("mockParseServerSequence requires at least one response")
	}
	calls := &atomic.Int32{}
	server := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		idx := int(calls.Add(1)) - 1
		if idx >= len(contents) {
			idx = len(contents) - 1
		}
		w.WriteHeader(http.StatusOK)
		_ = json.NewEncoder(w).Encode(openAIResponse{
			Choices: []struct {
				Message struct {
					Content          string `json:"content"`
					ReasoningContent string `json:"reasoning_content"`
				} `json:"message"`
				FinishReason string `json:"finish_reason"`
			}{
				{
					Message: struct {
						Content          string `json:"content"`
						ReasoningContent string `json:"reasoning_content"`
					}{Content: contents[idx]},
					FinishReason: "stop",
				},
			},
		})
	}))
	t.Cleanup(server.Close)

	mapper := testMapperWithTempCache(t, "lmstudio", &secrets.Provider{
		APIKey: "", Model: "test-model", BaseURL: server.URL,
	})
	mapper.client = server.Client()
	return mapper, calls
}

type roundTripFunc func(*http.Request) (*http.Response, error)

func (f roundTripFunc) RoundTrip(r *http.Request) (*http.Response, error) {
	return f(r)
}
